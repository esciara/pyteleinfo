def _build_valid_frame_json(data: list):
    json_ = {}
    for item in data:
        json_[item[LABEL_KEY]] = item[DATA_KEY]
    return json_


def _build_frame(data: list):
    frame = STX_TOKEN
    for item in data:
        frame = "".join([frame, encode_info_group(item[LABEL_KEY], item[DATA_KEY])])
    frame = "".join([frame, ETX_TOKEN])
    return frame


